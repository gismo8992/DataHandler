class PersonInfo {
    String surname;
    String name;
    String patronymic;
    String birthDate;
    long phoneNumber;
    char gender;

    public PersonInfo(String surname, String name, String patronymic, String birthDate, long phoneNumber, char gender) {
        this.surname = surname;
        this.name = name;
        this.patronymic = patronymic;
        this.birthDate = birthDate;
        this.phoneNumber = phoneNumber;
        this.gender = gender;
    }
}